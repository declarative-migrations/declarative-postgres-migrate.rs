#[tokio::test]
async fn cockroach_functions_procedures_and_triggers_converge_and_execute() {
    let Some(admin) = admin_url() else { return };
    let (source_db, target_db, migration) = migrate_and_assert_converges(
        &admin,
        ROUTINES_AND_TRIGGERS_SOURCE,
        ROUTINES_AND_TRIGGERS_TARGET,
        "routines-triggers",
    )
    .await;

    assert!(
        migration.contains("DROP TRIGGER IF EXISTS \"accounts_audit\""),
        "CockroachDB trigger functions must cycle their dependent triggers:\n{migration}"
    );
    assert!(
        migration.contains("CREATE OR REPLACE FUNCTION"),
        "{migration}"
    );
    assert!(
        migration.contains("CREATE OR REPLACE PROCEDURE"),
        "{migration}"
    );
    assert!(
        migration.contains("DROP PROCEDURE IF EXISTS \"public\".\"obsolete account job\"()"),
        "{migration}"
    );

    let mut conn = dpm::introspect::connect(&target_db.url).await.unwrap();
    sqlx::raw_sql(
        "INSERT INTO public.accounts (id, balance) VALUES (1, 10); \
         CALL public.credit_account(1, 5);",
    )
    .execute(&mut conn)
    .await
    .expect("execute migrated CockroachDB procedure and trigger");
    let behavior: (i64, bool, i64, i64) = sqlx::query_as(
        "SELECT a.balance, a.touched, public.multiply_values(3, 4), count(au.account_id)::INT8 \
           FROM public.accounts a \
           LEFT JOIN public.account_audit au ON au.account_id = a.id \
          WHERE a.id = 1 \
          GROUP BY a.balance, a.touched",
    )
    .fetch_one(&mut conn)
    .await
    .unwrap();
    assert_eq!(behavior, (15, true, 12, 1));

    source_db.drop_db().await;
    target_db.drop_db().await;
}

