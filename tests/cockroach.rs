#[tokio::test]
async fn cockroach_multiple_conditional_triggers_converge_and_execute() {
    let Some(admin) = admin_url() else { return };
    let source_sql = r#"
CREATE TABLE public.events (id INT PRIMARY KEY, value INT NOT NULL);
CREATE TABLE public.event_audit (event_id INT NOT NULL, observed_value INT NOT NULL);
CREATE FUNCTION public.normalize_event() RETURNS TRIGGER LANGUAGE PLpgSQL AS $$
BEGIN
  IF (NEW).value < 0 THEN
    NEW.value := 0;
  END IF;
  RETURN NEW;
END
$$;
CREATE FUNCTION public.audit_event() RETURNS TRIGGER LANGUAGE PLpgSQL AS $$
BEGIN
  INSERT INTO public.event_audit(event_id, observed_value)
    VALUES ((NEW).id, (NEW).value);
  RETURN NEW;
END
$$;
CREATE TRIGGER events_normalize BEFORE INSERT OR UPDATE ON public.events
  FOR EACH ROW EXECUTE FUNCTION public.normalize_event();
CREATE TRIGGER events_audit_insert AFTER INSERT ON public.events
  FOR EACH ROW WHEN ((NEW).value > 10)
  EXECUTE FUNCTION public.audit_event();
CREATE TRIGGER events_audit_update AFTER UPDATE ON public.events
  FOR EACH ROW WHEN ((NEW).value > 20)
  EXECUTE FUNCTION public.audit_event();
"#;
    let target_sql = r#"
CREATE TABLE public.events (id INT PRIMARY KEY, value INT NOT NULL);
CREATE TABLE public.event_audit (event_id INT NOT NULL, observed_value INT NOT NULL);
CREATE FUNCTION public.normalize_event() RETURNS TRIGGER LANGUAGE PLpgSQL AS $$
BEGIN
  IF (NEW).value < 0 THEN
    NEW.value := 99;
  END IF;
  RETURN NEW;
END
$$;
CREATE FUNCTION public.audit_event() RETURNS TRIGGER LANGUAGE PLpgSQL AS $$
BEGIN
  INSERT INTO public.event_audit(event_id, observed_value)
    VALUES ((NEW).id, -1);
  RETURN NEW;
END
$$;
CREATE TRIGGER events_normalize BEFORE INSERT OR UPDATE ON public.events
  FOR EACH ROW EXECUTE FUNCTION public.normalize_event();
CREATE TRIGGER events_audit_insert AFTER INSERT ON public.events
  FOR EACH ROW WHEN ((NEW).value > 10)
  EXECUTE FUNCTION public.audit_event();
CREATE TRIGGER events_audit_update AFTER UPDATE ON public.events
  FOR EACH ROW WHEN ((NEW).value > 20)
  EXECUTE FUNCTION public.audit_event();
CREATE TRIGGER events_audit_obsolete AFTER INSERT ON public.events
  FOR EACH ROW WHEN ((NEW).value > 1000)
  EXECUTE FUNCTION public.audit_event();
"#;

    let (source_db, target_db, migration) = migrate_and_assert_converges(
        &admin,
        source_sql,
        target_sql,
        "multiple-conditional-triggers",
    )
    .await;
    for name in [
        "events_normalize",
        "events_audit_insert",
        "events_audit_update",
        "events_audit_obsolete",
    ] {
        assert!(
            migration.contains(&format!("DROP TRIGGER IF EXISTS \"{name}\"")),
            "missing trigger dependency drop for {name}:\n{migration}"
        );
    }

    let mut conn = dpm::introspect::connect(&target_db.url).await.unwrap();
    sqlx::raw_sql(
        "INSERT INTO public.events (id, value) VALUES (1, -5), (2, 15), (3, 25); \
         UPDATE public.events SET value = 30 WHERE id = 2; \
         UPDATE public.events SET value = -7 WHERE id = 1;",
    )
    .execute(&mut conn)
    .await
    .expect("execute migrated CockroachDB conditional triggers");
    let values: (i64, i64, i64) = sqlx::query_as(
        "SELECT max(value) FILTER (WHERE id = 1)::INT8, \
                max(value) FILTER (WHERE id = 2)::INT8, \
                max(value) FILTER (WHERE id = 3)::INT8 \
           FROM public.events",
    )
    .fetch_one(&mut conn)
    .await
    .unwrap();
    let audit: (i64, i64) =
        sqlx::query_as("SELECT count(*)::INT8, sum(observed_value)::INT8 FROM public.event_audit")
            .fetch_one(&mut conn)
            .await
            .unwrap();
    assert_eq!(values, (0, 30, 25));
    assert_eq!(audit, (3, 70));

    source_db.drop_db().await;
    target_db.drop_db().await;
}

