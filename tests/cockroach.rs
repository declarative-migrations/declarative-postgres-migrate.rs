#[tokio::test]
async fn cockroach_cli_dump_diff_verify_and_dialect_guards() {
    let Some(admin) = admin_url() else { return };
    let source_db = fresh_db(&admin).await;
    let target_db = fresh_db(&admin).await;
    source_db.apply_sql(DESIRED_SCHEMA).await.unwrap();
    target_db.apply_sql(DIVERGENT_SCHEMA).await.unwrap();

    let scratch = std::env::temp_dir().join(format!(
        "dpm-cockroach-cli-{}",
        std::process::id()
    ));
    std::fs::create_dir_all(&scratch).unwrap();
    let dump_path = scratch.join("desired.json");
    let dump = Command::new(env!("CARGO_BIN_EXE_dpm"))
        .args(["dump", "--source", &source_db.url, "--out"])
        .arg(&dump_path)
        .output()
        .expect("dump CockroachDB catalog through the CLI");
    assert!(
        dump.status.success(),
        "CockroachDB dump failed:\nstdout:\n{}\nstderr:\n{}",
        String::from_utf8_lossy(&dump.stdout),
        String::from_utf8_lossy(&dump.stderr)
    );
    assert!(dump.stdout.is_empty(), "--out must keep stdout clean");
    let dumped: dpm::model::Catalog =
        serde_json::from_str(&std::fs::read_to_string(&dump_path).unwrap()).unwrap();
    assert_eq!(dumped.database_flavor, DatabaseFlavor::Cockroach);

    let plan = Command::new(env!("CARGO_BIN_EXE_dpm"))
        .args(["diff", "--format", "json", "--fail-on-diff", "--source-json"])
        .arg(&dump_path)
        .args(["--target", &target_db.url])
        .output()
        .expect("diff CockroachDB catalog against a live target");
    assert_eq!(
        plan.status.code(),
        Some(2),
        "stderr:\n{}",
        String::from_utf8_lossy(&plan.stderr)
    );
    let plan_json: serde_json::Value =
        serde_json::from_slice(&plan.stdout).expect("CLI must emit valid JSON");
    assert!(plan_json["summary"]["total"].as_u64().unwrap() > 0);
    assert!(plan_json["sql"]
        .as_str()
        .unwrap()
        .contains("dialect: CockroachDB"));

    let verified = Command::new(env!("CARGO_BIN_EXE_dpm"))
        .args(["verify", "--source-json"])
        .arg(&dump_path)
        .args([
            "--target",
            &target_db.url,
            "--shadow",
            &admin,
            "--allow-destructive-sql",
        ])
        .output()
        .expect("verify CockroachDB migration through the CLI");
    assert!(
        verified.status.success(),
        "CockroachDB verify failed:\nstdout:\n{}\nstderr:\n{}",
        String::from_utf8_lossy(&verified.stdout),
        String::from_utf8_lossy(&verified.stderr)
    );
    assert!(String::from_utf8_lossy(&verified.stderr).contains("VERIFIED"));

    let postgres_dump = scratch.join("postgres.json");
    let postgres_catalog = dpm::model::Catalog {
        database_flavor: DatabaseFlavor::Postgres,
        server_version_num: 160_000,
        ..Default::default()
    };
    std::fs::write(
        &postgres_dump,
        serde_json::to_string_pretty(&postgres_catalog).unwrap(),
    )
    .unwrap();
    let cross_dialect = Command::new(env!("CARGO_BIN_EXE_dpm"))
        .args(["diff", "--source-json"])
        .arg(&postgres_dump)
        .args(["--target", &source_db.url])
        .output()
        .expect("exercise the CLI cross-dialect guard");
    assert_eq!(cross_dialect.status.code(), Some(1));
    let error = String::from_utf8_lossy(&cross_dialect.stderr);
    assert!(error.contains("source is PostgreSQL but target is CockroachDB"), "{error}");

    source_db.drop_db().await;
    target_db.drop_db().await;
}

