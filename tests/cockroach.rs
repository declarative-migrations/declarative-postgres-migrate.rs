//! Integration coverage for CockroachDB's PostgreSQL-wire compatibility
//! surface.  These tests use a real CockroachDB server because catalog gaps
//! and DDL transaction behavior cannot be established from synthetic models.
//!
//! Set DPM_TEST_COCKROACH_DATABASE_URL to an admin URL whose role can CREATE
//! DATABASE (scripts/test-cockroach.sh starts an isolated single-node server).

use dpm::apply::apply_script;
use dpm::diff::diff;
use dpm::emit::{emit, EmitOptions};
use dpm::introspect::{introspect_url, IntrospectOptions};
use dpm::model::DatabaseFlavor;
use dpm::source::{resolve, ResolveContext, ShadowDb, SideSpec};
use dpm::verify::{verify, VerifyParams};
use std::process::Command;

fn admin_url() -> Option<String> {
    match std::env::var("DPM_TEST_COCKROACH_DATABASE_URL") {
        Ok(value) if !value.is_empty() => Some(value),
        _ => {
            eprintln!(
                "skipping: DPM_TEST_COCKROACH_DATABASE_URL not set \
                 (run scripts/test-cockroach.sh)"
            );
            None
        }
    }
}

