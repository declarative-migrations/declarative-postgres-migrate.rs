#[tokio::test]
async fn cockroach_sql_file_materializes_and_bootstraps_via_cli() {
    let Some(admin) = admin_url() else { return };
    let fixture = format!(
        "{}/tests/fixtures/cockroach_schema.sql",
        env!("CARGO_MANIFEST_DIR")
    );
    let opts = IntrospectOptions::default();
    let context = ResolveContext {
        introspect: &opts,
        shadow_url: Some(admin.clone()),
        keep_shadow: false,
        verbose: false,
    };
    let materialized = resolve(&SideSpec::SqlPath(fixture.clone()), &context)
        .await
        .expect("materialize CockroachDB SQL fixture");
    assert_eq!(materialized.database_flavor, DatabaseFlavor::Cockroach);

    let direct = fresh_db(&admin).await;
    direct
        .apply_sql(&std::fs::read_to_string(&fixture).unwrap())
        .await
        .unwrap();
    let direct_catalog = introspect_url(&direct.url, &opts).await.unwrap();
    assert!(
        diff(&materialized, &direct_catalog).is_empty(),
        "SQL materialization must match a directly-created CockroachDB schema"
    );

    let output = Command::new(env!("CARGO_BIN_EXE_dpm"))
        .args(["bootstrap", "--source-sql", &fixture, "--shadow", &admin])
        .output()
        .expect("run CockroachDB SQL-file bootstrap");
    assert!(
        output.status.success(),
        "CockroachDB bootstrap failed:\nstdout:\n{}\nstderr:\n{}",
        String::from_utf8_lossy(&output.stdout),
        String::from_utf8_lossy(&output.stderr)
    );
    let stdout = String::from_utf8_lossy(&output.stdout);
    assert!(stdout.contains("dialect: CockroachDB"), "{stdout}");
    assert!(
        stdout.contains("CREATE TABLE IF NOT EXISTS \"inventory\".\"products\""),
        "{stdout}"
    );

    direct.drop_db().await;
}
