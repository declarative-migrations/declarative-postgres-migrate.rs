async fn fresh_db(admin: &str) -> ShadowDb {
    ShadowDb::create(admin, false)
        .await
        .expect("create CockroachDB test database")
}

async fn migrate_and_assert_converges(
    admin: &str,
    source_sql: &str,
    target_sql: &str,
    label: &str,
) -> (ShadowDb, ShadowDb, String) {
    let source_db = fresh_db(admin).await;
    let target_db = fresh_db(admin).await;
    source_db
        .apply_sql(source_sql)
        .await
        .expect("populate desired CockroachDB schema");
    target_db
        .apply_sql(target_sql)
        .await
        .expect("populate current CockroachDB schema");

    let opts = IntrospectOptions::default();
    let source = introspect_url(&source_db.url, &opts)
        .await
        .expect("introspect desired CockroachDB schema");
    let target = introspect_url(&target_db.url, &opts)
        .await
        .expect("introspect current CockroachDB schema");
    assert_eq!(source.database_flavor, DatabaseFlavor::Cockroach);
    assert_eq!(target.database_flavor, DatabaseFlavor::Cockroach);

    let plan = diff(&source, &target);
    let script = emit(
        &plan,
        &EmitOptions {
            allow_destructive: true,
            database_flavor: DatabaseFlavor::Cockroach,
            ..Default::default()
        },
    );
    assert!(script.sql.contains("dialect: CockroachDB"));
    apply_script(&target_db.url, &script.sql)
        .await
        .unwrap_or_else(|error| {
            panic!(
                "[{label}] applying CockroachDB migration failed: {error:#}\n{}",
                script.sql
            )
        });

    let migrated = introspect_url(&target_db.url, &opts)
        .await
        .expect("re-introspect migrated CockroachDB schema");
    let residual = diff(&source, &migrated);
    assert!(
        residual.is_empty(),
        "[{label}] CockroachDB migration did not converge:\n{}\n--- migration ---\n{}",
        emit(
            &residual,
            &EmitOptions {
                database_flavor: DatabaseFlavor::Cockroach,
                ..Default::default()
            },
        )
        .sql,
        script.sql
    );

    (source_db, target_db, script.sql)
}

async fn assert_converges(admin: &str, source_sql: &str, target_sql: &str, label: &str) {
    let (source_db, target_db, _) =
        migrate_and_assert_converges(admin, source_sql, target_sql, label).await;
    source_db.drop_db().await;
    target_db.drop_db().await;
}

