fn scratch_path(name: &str) -> std::path::PathBuf {
    let dir = std::env::temp_dir().join(format!("dpm-combo-{}", std::process::id()));
    std::fs::create_dir_all(&dir).unwrap();
    dir.join(name)
}

async fn resolve_side(spec: &str, admin: &str) -> dpm::model::Catalog {
    let o = opts();
    let ctx = dpm::source::ResolveContext {
        introspect: &o,
        shadow_url: Some(admin.to_string()),
        keep_shadow: false,
        verbose: false,
    };
    dpm::source::resolve(&dpm::source::SideSpec::parse(spec).unwrap(), &ctx)
        .await
        .unwrap_or_else(|e| panic!("resolving {spec}: {e:#}"))
}

/// Every pairing of (url, .sql, .json) on both sides must produce the same
/// plan for the same underlying schemas.
#[tokio::test]
async fn all_side_kind_combinations_agree() {
    let Some(admin) = admin_url() else { return };

    // Materialize RICH as a live db + catalog.json; DIVERGENT likewise.
    let (source_db, target_db) = setup_pair(&admin, RICH_SCHEMA, DIVERGENT_SCHEMA).await;
    let source_json = scratch_path("source.json");
    let target_json = scratch_path("target.json");
    let source_sql = scratch_path("source.sql");
    let target_sql = scratch_path("target.sql");
    std::fs::write(&source_sql, RICH_SCHEMA).unwrap();
    std::fs::write(&target_sql, DIVERGENT_SCHEMA).unwrap();
    let source_cat = introspect_url(&source_db.url, &opts()).await.unwrap();
    let target_cat = introspect_url(&target_db.url, &opts()).await.unwrap();
    std::fs::write(&source_json, serde_json::to_string(&source_cat).unwrap()).unwrap();
    std::fs::write(&target_json, serde_json::to_string(&target_cat).unwrap()).unwrap();

    let source_kinds = [source_db.url.clone(), source_sql.display().to_string(), source_json.display().to_string()];
    let target_kinds = [target_db.url.clone(), target_sql.display().to_string(), target_json.display().to_string()];

    // Reference plan from url ↔ url.
    let reference = emit(&diff(&source_cat, &target_cat), &EmitOptions { allow_destructive: true, ..Default::default() });

    for s in &source_kinds {
        for t in &target_kinds {
            let sc = resolve_side(s, &admin).await;
            let tc = resolve_side(t, &admin).await;
            let script = emit(&diff(&sc, &tc), &EmitOptions { allow_destructive: true, ..Default::default() });
            assert_eq!(
                script.sql, reference.sql,
                "combo source={s} target={t} produced a different migration"
            );
        }
    }

    source_db.drop_db().await;
    target_db.drop_db().await;
}

/// A pg_dump --schema-only style dump (SET headers, psql \\restrict
/// meta-commands, OWNER TO / GRANT statements for roles that don't exist on
/// the shadow server) must materialize and diff clean against the live
/// database it was dumped from.
#[tokio::test]
async fn pg_dump_style_sql_file_materializes() {
    let Some(admin) = admin_url() else { return };
    let live = fresh_db(&admin).await;
    live.apply_sql("CREATE TABLE things (id bigserial PRIMARY KEY, name text NOT NULL);")
        .await
        .unwrap();

    let dump = r#"--
-- PostgreSQL database dump
--

\restrict h4xEcgNiOtVIatCVdEmDJRuqCXhZUAdOTgeCFyi0ZZuBcpNZZBLzB0oFAlwuvtA

SET statement_timeout = 0;
SET client_encoding = 'UTF8';
SELECT pg_catalog.set_config('search_path', '', false);
SET row_security = off;

CREATE TABLE public.things (
    id bigint NOT NULL,
    name text NOT NULL
);

ALTER TABLE public.things OWNER TO production_role_that_does_not_exist;

CREATE SEQUENCE public.things_id_seq
    START WITH 1
    INCREMENT BY 1
    NO MINVALUE
    NO MAXVALUE
    CACHE 1;

ALTER SEQUENCE public.things_id_seq OWNER TO production_role_that_does_not_exist;
ALTER SEQUENCE public.things_id_seq OWNED BY public.things.id;
ALTER TABLE ONLY public.things ALTER COLUMN id SET DEFAULT nextval('public.things_id_seq'::regclass);
ALTER TABLE ONLY public.things ADD CONSTRAINT things_pkey PRIMARY KEY (id);

GRANT ALL ON TABLE public.things TO some_app_role;

\unrestrict h4xEcgNiOtVIatCVdEmDJRuqCXhZUAdOTgeCFyi0ZZuBcpNZZBLzB0oFAlwuvtA

--
-- PostgreSQL database dump complete
--
"#;
    let dump_path = scratch_path("pg-dump-style.sql");
    std::fs::write(&dump_path, dump).unwrap();

    let from_dump = resolve_side(&dump_path.display().to_string(), &admin).await;
    let from_live = introspect_url(&live.url, &opts()).await.unwrap();
    let plan = diff(&from_dump, &from_live);
    let script = emit(&plan, &EmitOptions::default());
    assert!(
        plan.is_empty(),
        "dump-materialized catalog must match the live db it was dumped from:\n{}",
        script.sql
    );
    live.drop_db().await;
}

/// The destructive two-consent model: --allow-destructive-sql controls
/// generation; without --allow-destructive-ops the CLI apply path must refuse
/// to execute live destructive SQL (exercised at the library level here via
/// the same counts the CLI uses).
#[tokio::test]
async fn destructive_counts_drive_the_two_consent_gate() {
    let Some(admin) = admin_url() else { return };
    let (source_db, target_db) = setup_pair(&admin, "", "CREATE TABLE doomed (id int);").await;
    let source = introspect_url(&source_db.url, &opts()).await.unwrap();
    let target = introspect_url(&target_db.url, &opts()).await.unwrap();
    let plan = diff(&source, &target);

    // sql-consent off: everything destructive is gated; nothing live to run.
    let gated = emit(&plan, &EmitOptions::default());
    assert_eq!(gated.destructive_count, 1);
    assert_eq!(gated.gated_count, 1);
    assert_eq!(gated.destructive_count - gated.gated_count, 0);

    // sql-consent on: one live destructive statement — the ops gate (in the
    // CLI) must see live_destructive = 1.
    let live = emit(&plan, &EmitOptions { allow_destructive: true, ..Default::default() });
    assert_eq!(live.destructive_count - live.gated_count, 1);

    source_db.drop_db().await;
    target_db.drop_db().await;
}

// ===========================================================================
// Cross-checker matrix: every fixture pair is verified by dpm AND by every
// installed external tool (migra, pgdiff, atlas, pg-schema-diff, liquibase,
// apgdiff, flyway). Missing tools are skipped by --cross-check-all
// semantics, so the matrix degrades gracefully on machines without them.
// ===========================================================================

async fn verify_with_all_checkers(admin: &str, label: &str, source_sql: &str, target_sql: &str) {
    let (source_db, target_db) = setup_pair(admin, source_sql, target_sql).await;
    let source = introspect_url(&source_db.url, &opts()).await.unwrap();
    let target = introspect_url(&target_db.url, &opts()).await.unwrap();

    let outcome = dpm::verify::verify(dpm::verify::VerifyParams {
        source: &source,
        target: &target,
        shadow_server_url: admin,
        source_url_for_external: Some(&source_db.url),
        allow_destructive: true,
        external_check: None,
        checks: dpm::crosscheck::CheckSelection { all: true, ..Default::default() },
        bins: Default::default(),
        keep_shadow: false,
        verbose: false,
        introspect: &opts(),
    })
    .await
    .unwrap_or_else(|e| panic!("[{label}] verify failed: {e:#}"));

    assert!(
        outcome.converged,
        "[{label}] dpm did not converge:\n{}",
        outcome.residual_sql.as_deref().unwrap_or("")
    );
    let ran: Vec<&str> = outcome.checks.iter().map(|c| c.name.as_str()).collect();
    eprintln!("[{label}] cross-checkers ran: {ran:?}");
    for check in &outcome.checks {
        assert!(
            check.agreed,
            "[{label}] {} disagreed with dpm:\nerror: {:?}\noutput:\n{}\ncommand: {}",
            check.name, check.error, check.output, check.command
        );
    }

    source_db.drop_db().await;
    target_db.drop_db().await;
}

