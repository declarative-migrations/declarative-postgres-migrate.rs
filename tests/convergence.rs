//! Integration tests against a real Postgres server.
//!
//! Gated on DPM_TEST_DATABASE_URL (an admin URL where the role may CREATE
//! DATABASE, e.g. from `scripts/ephemeral-pg.sh start`). When unset, every
//! test is a silent pass so plain `cargo test` works offline.
//!
//! The gold-standard assertion is *convergence*: after applying the generated
//! migration to the target, re-diffing source vs target yields zero changes.

use dpm::apply::apply_script;
use dpm::diff::diff;
use dpm::emit::{emit, EmitOptions};
use dpm::introspect::{introspect_url, IntrospectOptions};
use dpm::source::{replace_database_in_url, ShadowDb};

fn admin_url() -> Option<String> {
    match std::env::var("DPM_TEST_DATABASE_URL") {
        Ok(v) if !v.is_empty() => Some(v),
        _ => {
            eprintln!("skipping: DPM_TEST_DATABASE_URL not set (run scripts/test.sh)");
            None
        }
    }
}

async fn fresh_db(admin: &str) -> ShadowDb {
    ShadowDb::create(admin, false).await.expect("create test database")
}

/// A schema exercising every object class dpm covers: enums, serial +
/// identity + generated columns, defaults, collations are skipped (portable),
/// PK/unique/check/FK constraints, partial + expression indexes, standalone
/// sequences, views, materialized views, functions, triggers, RLS + policies,
/// a second schema, and an extension.
const RICH_SCHEMA: &str = r#"
CREATE EXTENSION IF NOT EXISTS pgcrypto;
CREATE SCHEMA app;

CREATE TYPE public.mood AS ENUM ('sad', 'ok', 'happy');

CREATE SEQUENCE public.ticket_counter AS bigint INCREMENT BY 5 MINVALUE 1 MAXVALUE 9223372036854775807 START WITH 100 CACHE 1 NO CYCLE;

CREATE TABLE public.users (
  id bigserial NOT NULL,
  email text NOT NULL,
  current_mood public.mood NOT NULL DEFAULT 'ok',
  bio text,
  created_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT users_pkey PRIMARY KEY (id),
  CONSTRAINT users_email_key UNIQUE (email),
  CONSTRAINT users_bio_len CHECK (octet_length(bio) <= 10000)
);
CREATE INDEX users_created_idx ON public.users (created_at DESC);
CREATE UNIQUE INDEX users_email_lower_idx ON public.users (lower(email));

CREATE TABLE public.posts (
  id integer GENERATED ALWAYS AS IDENTITY,
  author_id bigint NOT NULL,
  title text NOT NULL,
  title_len integer GENERATED ALWAYS AS (length(title)) STORED,
  published boolean NOT NULL DEFAULT false,
  CONSTRAINT posts_pkey PRIMARY KEY (id),
  CONSTRAINT posts_author_fkey FOREIGN KEY (author_id) REFERENCES public.users(id) ON DELETE CASCADE
);
CREATE INDEX posts_published_partial_idx ON public.posts (author_id) WHERE published;

CREATE TABLE app.audit_log (
  id bigint GENERATED BY DEFAULT AS IDENTITY,
  actor bigint,
  action text NOT NULL,
  at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT audit_log_pkey PRIMARY KEY (id)
);

ALTER TABLE public.posts ENABLE ROW LEVEL SECURITY;
CREATE POLICY posts_owner_select ON public.posts AS PERMISSIVE FOR SELECT USING (published);

CREATE VIEW public.published_posts AS
  SELECT p.id, p.title, u.email AS author_email
  FROM public.posts p JOIN public.users u ON u.id = p.author_id
  WHERE p.published;

CREATE MATERIALIZED VIEW public.mood_counts AS
  SELECT current_mood, count(*) AS n FROM public.users GROUP BY current_mood;

CREATE FUNCTION public.touch_created() RETURNS trigger AS $fn$
BEGIN
  NEW.created_at := now();
  RETURN NEW;
END;
$fn$ LANGUAGE plpgsql;

CREATE TRIGGER users_touch BEFORE INSERT ON public.users
  FOR EACH ROW EXECUTE FUNCTION public.touch_created();
"#;

/// A divergent evolution of the same database: everything above plus/minus —
/// enum value inserted mid-list, columns added/removed/retyped, constraint
/// changed, index changed, view changed, function body changed, trigger
/// dropped, policy changed, new table, dropped table.
const DIVERGENT_SCHEMA: &str = r#"
CREATE EXTENSION IF NOT EXISTS pgcrypto;
CREATE SCHEMA app;

CREATE TYPE public.mood AS ENUM ('sad', 'ok', 'happy');

CREATE TABLE public.users (
  id bigserial NOT NULL,
  email text NOT NULL,
  current_mood public.mood NOT NULL DEFAULT 'sad',
  legacy_flag boolean DEFAULT true,
  created_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT users_pkey PRIMARY KEY (id),
  CONSTRAINT users_bio_len CHECK (octet_length(email) <= 5000)
);
CREATE INDEX users_created_idx ON public.users (created_at);

CREATE TABLE public.stale_stats (
  day date NOT NULL,
  hits integer NOT NULL DEFAULT 0,
  CONSTRAINT stale_stats_pkey PRIMARY KEY (day)
);

CREATE VIEW public.published_posts AS
  SELECT u.id, u.email AS author_email FROM public.users u;

CREATE FUNCTION public.touch_created() RETURNS trigger AS $fn$
BEGIN
  NEW.created_at := clock_timestamp();
  RETURN NEW;
END;
$fn$ LANGUAGE plpgsql;
"#;

async fn setup_pair(admin: &str, source_sql: &str, target_sql: &str) -> (ShadowDb, ShadowDb) {
    let source = fresh_db(admin).await;
    let target = fresh_db(admin).await;
    source.apply_sql(source_sql).await.expect("populate source");
    target.apply_sql(target_sql).await.expect("populate target");
    (source, target)
}

fn opts() -> IntrospectOptions {
    IntrospectOptions::default()
}

/// Core invariant: migrate target → re-diff → empty plan.
async fn assert_converges(admin: &str, source_sql: &str, target_sql: &str, label: &str) {
    let (source_db, target_db) = setup_pair(admin, source_sql, target_sql).await;

    let source = introspect_url(&source_db.url, &opts()).await.expect("introspect source");
    let target = introspect_url(&target_db.url, &opts()).await.expect("introspect target");

    let plan = diff(&source, &target);
    let script = emit(&plan, &EmitOptions { allow_destructive: true, ..Default::default() });

    apply_script(&target_db.url, &script.sql)
        .await
        .unwrap_or_else(|e| panic!("[{label}] applying generated migration failed: {e:#}\n--- script ---\n{}", script.sql));

    let migrated = introspect_url(&target_db.url, &opts()).await.expect("re-introspect target");
    let residual = diff(&source, &migrated);
    let residual_script = emit(&residual, &EmitOptions::default());
    assert!(
        residual.is_empty(),
        "[{label}] did not converge; residual changes:\n{}\n--- original script ---\n{}",
        residual_script.sql,
        script.sql
    );

    source_db.drop_db().await;
    target_db.drop_db().await;
}

